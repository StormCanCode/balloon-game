function lvl () {
    if (lvl2 == 0) {
        scene.setBackgroundColor(7)
        tiles.setCurrentTilemap(tilemap`level0`)
        dart = 1
        info.setLife(3)
        info.setScore(0)
        enemy()
        game.splash("level 1", "a to shoot, arrows to move")
        dcnt = 0
    } else if (lvl2 == 1) {
        dart = 2
        enemy()
        info.setLife(3)
        info.setScore(0)
        tiles.setCurrentTilemap(tilemap`level2`)
        game.splash("level 2", "fast dart unlocked")
        dcnt = 0
    } else if (lvl2 == 2) {
        dart = 3
        dcnt = 0
        tiles.setCurrentTilemap(tilemap`level5`)
        game.splash("level 3", "GUNN!!!!")
        game.splash("the balloons are coming from the box", "pop 'em")
        info.setLife(3)
        info.setScore(0)
        enemy()
    } else if (lvl2 == 3) {
        dart = 3
        dcnt = 0
        tiles.setCurrentTilemap(tilemap`level6`)
        game.splash("level 4", "GUNN go brrrrrr")
    } else if (lvl2 == 4) {
        dcnt = 0
    }
    tiles.placeOnRandomTile(mySprite, sprites.dungeon.collectibleInsignia)
}
